'''
Listas são usadas para armazenar uma coleção ordenada de itens.
Os itens em uma lista podem ser de diferentes tipos e podem ser 
acessados através de índices.
'''

# Declarando uma lista
numeros = [1, 2, 3, 4, 5]

# Acessando elementos da lista
print("Primeiro número:", numeros[0])
print("Último número:", numeros[-1])

# Iterando sobre a lista
for numero in numeros:
    print(numero)